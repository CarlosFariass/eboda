  /**
   * Função auxiliar para gerar URL amigável a partir do nome da cor
   */
  export const getColorSlug = (colorName) => {
    return colorName
      .toLowerCase()
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '')
      .replace(/\s+/g, '-')
      .replace(/[^\w-]/g, '');
  };
  
  /**
   * Função auxiliar para encontrar uma cor pelo slug
   */
  export const getColorBySlug = (slug) => {
    return POPULAR_COLORS.find((color) => getColorSlug(color.name) === slug);
  };